subroutine ylm(V,Y,LMAX)
implicit none
INTEGER :: LMAX
real*8 :: V(3)
complex*16 :: Y((LMAX+1)*(LMAX+1))
integer :: I2L,I4L2,INDEX,INDEX2,L,M,MSIGN
REAL*8 :: A,B,C,AB,ABC,ABMAX,ABCMAX
REAL*8 :: D4LL1C,D2L13,PI
REAL*8 :: COSTH,SINTH,COSPH,SINPH
REAL*8 :: TEMP1,TEMP2,TEMP3
REAL*8 :: YLLR,YLL1R,YL1L1R,YLMR
REAL*8 :: YLLI,YLL1I,YL1L1I,YLMI

PI=4.0D0*ATAN(1.0D0)
YLLR=1.0D0/SQRT(4.0D0*PI)
YLLI=0.0D0
Y(1)=DCMPLX(YLLR,YLLI)
ABMAX=MAX(ABS(V(1)),ABS(V(2)))
IF(ABMAX.GT.0.0D0)THEN
A=V(1)/ABMAX
B=V(2)/ABMAX
AB=SQRT(A*A+B*B)
COSPH=A/AB
SINPH=B/AB
ELSE
COSPH=1.0D0
SINPH=0.0D0
ENDIF
ABCMAX=MAX(ABMAX,ABS(V(3)))
IF(ABCMAX.GT.0.0D0)THEN
A=V(1)/ABCMAX
B=V(2)/ABCMAX
C=V(3)/ABCMAX
AB=A*A+B*B
ABC=SQRT(AB+C*C)
COSTH=C/ABC
SINTH=SQRT(AB)/ABC
ELSE
COSTH=1.0D0
SINTH=0.0D0
ENDIF
Y(3)=DCMPLX(SQRT(3.0D0)*YLLR*COSTH,0.0D0)
TEMP1=-SQRT(1.5D0)*YLLR*SINTH
Y(4)=DCMPLX(TEMP1*COSPH,TEMP1*SINPH)
Y(2)=-DCONJG(Y(4))
DO L=2,LMAX
INDEX=L*L+1
INDEX2=INDEX+2*L
MSIGN=1-2*MOD(L,2)
! YLL=Y(L,L)=F(Y(L-1,L-1)) ... FORMULA 1
YL1L1R=DBLE(Y(INDEX-1))
YL1L1I=DIMAG(Y(INDEX-1))
TEMP1=-SQRT(DBLE(2*L+1)/DBLE(2*L))*SINTH
YLLR=TEMP1*(COSPH*YL1L1R-SINPH*YL1L1I)
YLLI=TEMP1*(COSPH*YL1L1I+SINPH*YL1L1R)
Y(INDEX2)=DCMPLX(YLLR,YLLI)
Y(INDEX)=MSIGN*DCONJG(Y(INDEX2))
INDEX2=INDEX2-1
INDEX=INDEX+1
! YLL1=Y(L,L-1)=F(Y(L-1,L-1)) ... FORMULA 2
TEMP2=SQRT(DBLE(2*L+1))*COSTH
YLL1R=TEMP2*YL1L1R
YLL1I=TEMP2*YL1L1I
Y(INDEX2)=DCMPLX(YLL1R,YLL1I)
Y(INDEX)=-MSIGN*DCONJG(Y(INDEX2))
INDEX2=INDEX2-1
INDEX=INDEX+1
I4L2=INDEX2-4*L+2
I2L=INDEX2-2*L
D4LL1C=COSTH*SQRT(DBLE(4*L*L-1))
D2L13=-SQRT(DBLE(2*L+1)/DBLE(2*L-3))
DO M=L-2,0,-1
! YLM=Y(L,M)=F(Y(L-2,M),Y(L-1,M)) ...FORMULA 2
TEMP1=1.0D0/SQRT(DBLE((L+M)*(L-M)))
TEMP2=D4LL1C*TEMP1
TEMP3=D2L13*SQRT(DBLE((L+M-1)*(L-M-1)))*TEMP1
YLMR=TEMP2*DBLE(Y(I2L))+TEMP3*DBLE(Y(I4L2))
YLMI=TEMP2*DIMAG(Y(I2L))+TEMP3*DIMAG(Y(I4L2))
Y(INDEX2)=DCMPLX(YLMR,YLMI)
Y(INDEX)=MSIGN*DCONJG(Y(INDEX2))
MSIGN=-MSIGN
INDEX2=INDEX2-1
INDEX=INDEX+1
I4L2=I4L2-1
I2L=I2L-1
END DO
END DO

RETURN
END

subroutine suml(yl,tcc,lm)
implicit none
complex*16 :: yl(81)
complex*16 :: tcc
integer :: lm(2)
integer :: l,m
complex*16 :: imag,imag1
real*8 :: minu
integer :: ly1,ly2

imag=(0.0d0,1.0d0)
imag1=(1.0d0,0.0d0)
minu=1.0d0
l=lm(1)
m=lm(2)
if(l.lt.0)then
imag1=-imag
minu=-1.0d0
l=iabs(l)
end if
if(mod(m,2).eq.1)then
imag1=-imag1
minu=-minu
end if
ly1=l*(l+1)+m+1
if(m.eq.0)then
tcc=imag1*yl(ly1)
else 
ly2=l*(l+1)-m+1
tcc=imag1*(yl(ly1)+yl(ly2)*minu)/sqrt(2.0d0)
end if
if(aimag(tcc).gt.1.0d-8)write(6,*)lm(1),lm(2),tcc
return
end

